Basic_salary = float(input('Enter the value for Basic_salary: '))

Allowances = float(input('Enter the value for Allowances: '))

Deductions = float(input('Enter the value for Deductions: '))

Net_Salary = (Basic_salary + Allowances - Deductions)

print('Your Net Salary, based on the values you inputted will be: ', Net_Salary)
